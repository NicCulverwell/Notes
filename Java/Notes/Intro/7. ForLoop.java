package Intro;

class ForLoop {

    public static void main(String []args) {

        // For loops allow us to simplify massive amounts of code, to a simple 4 or 5 lines

        // for initialisation; termination; update
        // this means from x to y, increasing in implements of z, do the following

        for(int i = 1;i < 10; i += 2){
            System.out.println(i);
        }

        // this means for all values of i, begining at 1, and ending at less than 10, additively increasing by 2, print out the value of i
    }

    
}
