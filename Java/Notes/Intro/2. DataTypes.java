package Intro;

class DataTypes {
    
    public static void main(String [] args) {

        // Strings:

        String name = "Nicholas";                           // Double Quotation Mark for Strings
        System.out.println("My name is " + name);

        // Characters

        char initial = 'N';                                 // Single Quotation Mark for Characters
        System.out.println("my initial is " + initial);

        // Integers
        int age = 20;                                       // No quotations required
        System.out.println("I am " + age + " years old");

        // Doubles
        double pi = 3.141526;                               // Doubles are usually used for decimal places, as it can go up to 15 decimal places
        System.out.println("the numerical number for pi, rounded off to 6 digits is: " + pi);

        // Booleans
        boolean isMale = true;
        System.out.println("am i male? " + isMale);

        


    }

}
