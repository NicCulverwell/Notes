package JCF;

class VectorAndStack {

    public static void main(String [] args) {

        // Vectors act similarly to ArrayList
        // - contain synchronised methods for accessing and modifying the vector
        // - Vector()                                   - creates a default empty vector with initial capacity of 10
        // - Vector(Collection)                         - creates a vector from an existing collection
        // - Vector(InitialCapacity)                    - creates a default empty vector with a specified initial capacity
        // - Vector(InitialCapacity, CapacityIncrement) - creates a vector with a specified initial capacity and increment
        // - addElement(Object)                         - appends the element to the end of the vector
        // - capacity()                                 - returns the current capacity of the vector
        // - copyInto(AnArray)                          - copies the elements in this vector to the array
        // - elementAt(index)                           - returns the object at the specified array
        // - elements()                                 - returns an enumeration of this vector 
        // - ensureCapacity()                           - increase the capacity of this vector
        // - firstElement()                             - returns the first element in the vector
        // - insertElementAt(object, index)             - inserts an object at the specified index
        // - lastElement()                              - returns the last element in the vector
        // - removeAllElements()                        - removes all elements from the vector 
        // - removeElement(object)                      - removes a specified element
        // - removeElementAt(index)                     - removes the element at a specified index
        // - setElementAt(object, index)                - set a specific element at a specified index
        // - setSize(newSize)                           - changes the size of the vector
        // - trimToSize()                               - trims the size of the vector to its current size

        // Stacks
        // - represent a last-in-last-out stack of objects
        // - the elements are only accessed from the top of the stack
        // - you can retrieve insert or remove elements from the stack
    
        // - Stack()        - creates a new empty stack
        // - empty()        - returns true if the stack is empty
        // - peek()         - returns the top element of the stack
        // - pop()          - returns and removes the top element of the stack
        // - push()         - adds a new element to the top of the stack
        // - search(object) - returns the position of the specified object in the stack
    }   

}
