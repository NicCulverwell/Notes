""" Data Type Conversions

int() Function
    takes a float or string input
    Outputs a integer
    y = int(x)

float() Function
    takes an integer or string input
    Outputs a float
    y = float(x)

string() Function
    takes an integer or a float input
    Outputs a string
    y = string(x)
"""

x = "5"

x = int(x)

print(x)

x = float(x)

print(x)

x = str(x)

print(x)