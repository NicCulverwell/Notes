""" Math Module
import math
    math functions are not built in functions
        they must be imported in order to be used

Functions
    math.ceil(x): return the ceiling of x as a float
        ceil(21.56) = 22 - always rounds up

    math.floor(x): returns the floor of x as a float
        ceil(21.56) = 22 - always round down

    math.exp(x): returns e ** x
        e will always be the base

    math.log(x, y): returns the logarithm of x 
        y is the base

    math.log10(x): returns the logarithm of x
        the base is a constant of 10
    
    math.pow(x, y): returns x ** y
        the first variable is the base

    math.sqrt(x): returns the square root of x
        do not square root a negative number

    math.degrees(x): converts angle x from radians to degrees

    math.radians(x): converts angle x from degrees to radians

    math.sin(x): returns the sine of x radians

    math.cos(x): returns the cosine of x radians

    math.tan(x): returns the tangent of x radians

    math.asin(x): returns the arc sine  of x radians

    math.acos(x): returns the arc cosine of x radians

    math.atan(x): returns the arc tangent of x radians

    math.pi: returns the value of pi (3.1415...)

    math.e: returns the value of e (2.7182...)
    
"""