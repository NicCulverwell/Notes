package Data;

class Maps {
    
        // Maps:
        // An instance of Map represents a group of Objects, each of which is associated with a key
        // you can get the object from the map using a key, and you have to use a key to get an object into a map

        // The map interface maps keys to elements
        //  - the keys are like indices

        // Default Functions of a Map:
            // put(x,y) adds a new key-value pair to the map
            // remove(x) removes ALL entries of that key
            // get(x) returns the value associated with the given key
            // isEmpty() returns true if the Map contains no key-value pairs
            // size() returns an integer representing the size of the map
            // keySet() returns a set of all the keys in the map
            // values() returns a collection of all the values in the map

        // There are 3 Types of Maps:
            // TreeMaps    
            // Hashmaps
            // Linkedmaps

}
